# Listas
# Criação e Acesso a Elementos:

# Crie uma lista de 5 frutas diferentes.
frutas = ['banana', 'maca', 'laranja', 'pera', 'abacate']

# Acesse e imprima a terceira fruta da lista.
print(frutas[2])

# Adição e Remoção de Elementos:

# Adicione a fruta "morango" à lista de frutas criada anteriormente.
frutas.append('morango')

# Remova a primeira fruta da lista e imprima a lista atualizada.
frutas.pop(0)

print(frutas)

# Ordenação e Reversão:

# Ordene a lista de frutas em ordem alfabética e imprima a lista ordenada.
print(sorted(frutas))

# Inverta a ordem dos elementos na lista e imprima a lista invertida.
print(frutas.reverse())


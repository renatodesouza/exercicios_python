#Forma usada para strings
nome = 'renato'

print('Meu nome é %s' % nome)

#Forma usada para numeros inteiros
idade = 40

print('Minha idade é %d' % idade)

#Forma usada para numeros de ponto flutuante
peso = 88

print('Meu peso é %.2f' % peso)

#Combinação

print('Meu nome é %s, tenho %d anos e peso %.2f kilos' % (nome, idade, peso))